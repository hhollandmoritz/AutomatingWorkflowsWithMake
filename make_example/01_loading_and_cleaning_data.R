#### Read in data 
#### =================================================================================================== ####
# Read in data

iris.raw <- read.table("iris.txt", sep = "\t", header = TRUE)

#### =================================================================================================== ####


# Change column names
#### =================================================================================================== ####
iris <- iris.raw
names(iris) <- c("SepalLength", "SepalWidth", "PetalLength", "PetalWidth", "Species")

#### =================================================================================================== ####

#### Separate out setosa data
#### =================================================================================================== ####
iris.set <- subset(iris, Species == "setosa")

#### =================================================================================================== ####

#### Save Data and Figures
#### =================================================================================================== ####

# Save full data
saveRDS(iris, file = "iris.RDS")

# Save subsetted data
saveRDS(iris.set, file = "iris.set.RDS")

